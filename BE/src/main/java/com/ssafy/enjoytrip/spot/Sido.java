package com.ssafy.enjoytrip.spot;

public class Sido {
	String sidoid;
	String name;
	public String getSidoid() {
		return sidoid;
	}
	public void setSidoid(String sidoid) {
		this.sidoid = sidoid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "Sido [sidoid=" + sidoid + ", name=" + name + "]";
	}
	
}
